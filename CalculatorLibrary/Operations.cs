namespace CalculatorLibrary
{
    public class Operations
    {
        public int Add(int num1, int num2)
        {
            int value = num1 + num2;
            return value;
        }

        public int Substract(int num1, int num2)
        {
            int value = num1 - num2;
            return value;
        }

        public int Multipy(int num1, int num2)
        {
            int value = num1 * num2;
            return value;
        }

        public int Divide(int num1, int num2)
        {
            int value = num1 / num2;
            return value;
        }
    }
}
